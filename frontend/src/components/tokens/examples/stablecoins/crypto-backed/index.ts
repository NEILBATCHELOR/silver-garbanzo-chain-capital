// Crypto-Backed Stablecoins
// 
// Stablecoins backed by cryptocurrency collateral, typically overcollateralized
// to account for the volatility of the underlying crypto assets.
//
// Categories:
// - Primary: Multi-standard implementations with complex mechanisms
// - Alternative: Simplified vault-based implementations
//
// Key Characteristics:
// - Overcollateralization (typically 150-200%)
// - Decentralized governance and operation
// - Algorithmic liquidation mechanisms
// - Yield-bearing capabilities
// - Higher capital efficiency than fiat-backed
//
// Risk Factors:
// - Collateral volatility risk
// - Smart contract risk
// - Liquidation cascade risk
// - Governance risk
// - Oracle manipulation risk
//
// Market Examples:
// - DAI (MakerDAO) - $5.3B+ market cap
// - FRAX (Frax Protocol) - Fractional-algorithmic
// - LUSD (Liquity) - Immutable protocol
// - sUSD (Synthetix) - SNX collateralized
